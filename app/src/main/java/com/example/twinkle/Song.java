package com.example.twinkle;

public class Song {  //大势实体类

    private String SongID;
    private String SongPath;
    private String SongName;
    private String WriterName;
    private String ProducerName;
    private String SongImage;
    private String Length;
    private int Playtimes;
    private String AlbumID;
    private String SongSchool;
    private String SongAge;
    public String getSongID() {
        return SongID;
    }
    public void setSongID(String songID) {
        SongID = songID;
    }
    public String getSongPath() {
        return SongPath;
    }
    public void setSongPath(String songPath) {
        SongPath = songPath;
    }
    public String getSongName() {
        return SongName;
    }
    public void setSongName(String songName) {
        SongName = songName;
    }
    public String getWriterName() {
        return WriterName;
    }
    public void setWriterName(String writerName) {
        WriterName = writerName;
    }
    public String getProducerName() {
        return ProducerName;
    }
    public void setProducerName(String producerName) {
        ProducerName = producerName;
    }
    public String getSongImage() {
        return SongImage;
    }
    public void setSongImage(String songImage) {
        SongImage = songImage;
    }
    public String getLength() {
        return Length;
    }
    public void setLength(String length) {
        Length = length;
    }
    public int getPlaytimes() {
        return Playtimes;
    }
    public void setPlaytimes(int playtimes) {
        Playtimes = playtimes;
    }
    public String getAlbumID() {
        return AlbumID;
    }
    public void setAlbumID(String albumID) {
        AlbumID = albumID;
    }
    public String getSongSchool() {
        return SongSchool;
    }
    public void setSongSchool(String songSchool) {
        SongSchool = songSchool;
    }
    public String getSongAge() {
        return SongAge;
    }
    public void setSongAge(String songAge) {
        SongAge = songAge;
    }
}
